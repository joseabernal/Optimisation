function f = GoldsteinPrice()
    f1 = @(x) (x(1) + x(2) + 1)^2;
    f2 = @(x) (19 - 14*x(1) + 3*x(1)^2 - 14*x(2) + 6*x(1)*x(2) + 3*x(2)^2);
    f3 = @(x) (2*x(1) - 3*x(2))^2;
    f4 = @(x) (18 - 32*x(1) + 12*x(1) + 48*x(2) - 36*x(1)*x(2) + 27*x(2)^2);
    f = @(x) (1 + f1(x) * f2(x)) * (30 + f3(x) * f4(x));
end